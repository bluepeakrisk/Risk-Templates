def get_industry_archetype(industry_label: str) -> str:
    """Map a UI industry label to one of 7 archetype codes; default to PROF."""
    return INDUSTRY_MAPPING.get(industry_label, "PROF")


def get_risks_for_industry(archetype: str = "PROF") -> list:
    """Return all 32 risks with content tailored to the given archetype.

    Each entry returned is a flat dict ready for the Excel writer.
    """
    out = []
    for risk in RISK_LIBRARY:
        v = risk["industries"].get(archetype) or risk["industries"]["PROF"]
        out.append({
            "id": risk["id"],
            "domain": risk["domain"],
            "theme": risk["theme"],
            "treatment": risk["treatment"],
            "title": v["title"],
            "description": v["description"],
            "causes": (
                f"PEOPLE: {v['causes_people']}\n"
                f"PROCESS: {v['causes_process']}\n"
                f"SYSTEMS: {v['causes_systems']}\n"
                f"EXTERNAL: {v['causes_external']}"
            ),
            "consequence": v["consequence"],
            "inh_likelihood": v["inh_likelihood"],
            "inh_impact": v["inh_impact"],
            "impact_rationale": v["impact_rationale"],
            "owner_suggested": v["owner_suggested"],
        })
    return out


